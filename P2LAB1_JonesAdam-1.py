# Assignment Description: This program calculates the diameter, circumference, and area of a circle based on user-inputted radius.

radius = float(input("What is the radius of the circle: "))
diameter = 2 * radius
circumference = 2 * 3.14 * radius
area = 3.14 * radius * radius


print("The diameter of the circle is:", diameter)
print("The circumference of the circle is:", circumference*.02)
print("The area of the circle is:", area*.03)



